func intProperty(properties map[string]any, key string, fallback int) int {
	if properties == nil {
		return fallback
	}
	value, ok := properties[key]
	if !ok {
		return fallback
	}
	intValue, ok := value.(int)
	if !ok {
		return fallback
	}
	return intValue
}

func hasInternetALBEdge(g graph.Graph, albID string) bool {
	for _, edge := range g.Edges {
		if graph.IsInternetFacingNetworkAllow(g, edge) && edge.To == albID && edge.Protocol == "tcp" && edge.Port == 443 {
			return true
		}
	}
	return false
}

func hasNetworkAllowEdge(g graph.Graph, from string, to string, port int) bool {
	for _, edge := range g.Edges {
		if edge.From == from && edge.To == to && edge.Kind == "network.allow" && edge.Protocol == "tcp" && edge.Port == port {
			return true
		}
	}
	return false
}

func effectiveProfile(g graph.Graph) string {
	profile := strings.TrimSpace(g.Profile)
	if profile == "" {
		return "lab"
	}
	return profile
}

func sortedStrings(values []string) []string {
	out := append([]string(nil), values...)
	slices.Sort(out)
	return out
}

func markdownCode(value string) string {
	clean := markdownInline(value)
	backticks := longestBacktickRun(clean) + 1
	fence := strings.Repeat("`", backticks)
	if strings.Contains(clean, "`") {
		return fence + " " + clean + " " + fence
	}
	return fence + clean + fence
}

func markdownInline(value string) string {
	value = strings.ReplaceAll(value, "\r\n", " ")
	value = strings.ReplaceAll(value, "\n", " ")
	value = strings.ReplaceAll(value, "\r", " ")
	value = strings.ReplaceAll(value, "\t", " ")
	return strings.Join(strings.Fields(value), " ")
}

func longestBacktickRun(value string) int {
	longest := 0
	current := 0
	for _, char := range value {
		if char == '`' {
			current++
			if current > longest {
				longest = current
			}
			continue
		}
		current = 0
	}
	return longest
}
