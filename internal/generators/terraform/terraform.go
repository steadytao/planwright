func Render(g graph.Graph) ([]artifact.File, error) {
	webapp, err := inspectWebApp(g)
	if err != nil {
		return nil, err
	}

	files := []artifact.File{
		{Path: "README.md", Data: []byte(renderReadme(g))},
		{Path: "versions.tf", Data: []byte(renderVersions())},
		{Path: "providers.tf", Data: []byte(renderProviders(g))},
		{Path: "variables.tf", Data: []byte(renderVariables(effectiveProfile(g)))},
		{Path: "network.tf", Data: []byte(renderNetwork())},
		{Path: "security-groups.tf", Data: []byte(renderSecurityGroups(webapp))},
		{Path: "iam.tf", Data: []byte(renderIAM())},
		{Path: "observability.tf", Data: []byte(renderObservability())},
		{Path: "app.tf", Data: []byte(renderApp(webapp))},
		{Path: "database.tf", Data: []byte(renderDatabase(webapp))},
		{Path: "outputs.tf", Data: []byte(renderOutputs())},
	}
	artifact.Sort(files)
	return files, nil
}

type webAppGraph struct {
	ALB     graph.Node
	App     graph.Node
	DB      graph.Node
	Region  string
	AppPort int
	DBPort  int
}

func inspectWebApp(g graph.Graph) (webAppGraph, error) {
	if g.Provider != "aws" {
		return webAppGraph{}, fmt.Errorf("terraform generator supports aws graphs only")
	}
	if profile := strings.TrimSpace(g.Profile); profile != "" && profile != "lab" {
		return webAppGraph{}, fmt.Errorf("terraform generator currently supports lab profile graphs only")
	}

	var albs []graph.Node
	var apps []graph.Node
	var databases []graph.Node
	var unsupported []string
	for _, node := range g.Nodes {
		switch node.Kind {
		case "aws.alb":
			albs = append(albs, node)
		case "aws.ecs.service":
			apps = append(apps, node)
		case "aws.rds.postgres":
			databases = append(databases, node)
		case "external.internet":
			continue
		default:
			unsupported = append(unsupported, node.Kind)
		}
	}

	if len(albs) != 1 || len(apps) != 1 || len(databases) != 1 {
		return webAppGraph{}, fmt.Errorf("terraform generator requires aws.alb, aws.ecs.service and aws.rds.postgres nodes exactly once; got aws.alb=%d, aws.ecs.service=%d, aws.rds.postgres=%d", len(albs), len(apps), len(databases))
	}
	if len(unsupported) > 0 {
		return webAppGraph{}, fmt.Errorf("terraform generator supports the single AWS web application pattern only; unsupported node kinds: %s", strings.Join(sortedStrings(unsupported), ", "))
	}

	result := webAppGraph{
		ALB:     albs[0],
		App:     apps[0],
		DB:      databases[0],
		Region:  strings.TrimSpace(g.Region),
		AppPort: intProperty(apps[0].Properties, "port", 8080),
		DBPort:  intProperty(databases[0].Properties, "port", 5432),
	}
	if result.AppPort < limits.MinNetworkPort || result.AppPort > limits.MaxNetworkPort {
		return webAppGraph{}, fmt.Errorf("application port %d is outside %d..%d", result.AppPort, limits.MinNetworkPort, limits.MaxNetworkPort)
	}
	if result.DBPort < limits.MinNetworkPort || result.DBPort > limits.MaxNetworkPort {
		return webAppGraph{}, fmt.Errorf("database port %d is outside %d..%d", result.DBPort, limits.MinNetworkPort, limits.MaxNetworkPort)
	}
	if public, ok := graph.BoolProperty(result.DB.Properties, "publicly_accessible"); ok && public {
		return webAppGraph{}, fmt.Errorf("terraform generator does not support publicly accessible database nodes")
	}
	if !hasInternetALBEdge(g, result.ALB.ID) {
		return webAppGraph{}, fmt.Errorf("terraform generator requires an internet-facing network.allow edge to %s on tcp/443", result.ALB.ID)
	}
	if !hasNetworkAllowEdge(g, result.ALB.ID, result.App.ID, result.AppPort) {
		return webAppGraph{}, fmt.Errorf("terraform generator requires a network.allow edge from %s to %s on tcp/%d", result.ALB.ID, result.App.ID, result.AppPort)
	}
	if !hasNetworkAllowEdge(g, result.App.ID, result.DB.ID, result.DBPort) {
		return webAppGraph{}, fmt.Errorf("terraform generator requires a network.allow edge from %s to %s on tcp/%d", result.App.ID, result.DB.ID, result.DBPort)
	}
	return result, nil
}

func renderReadme(g graph.Graph) string {
	return fmt.Sprintf(
		"# Generated Terraform/OpenTofu Review Pack\n\n"+
			"This directory was generated by Planwright for review.\n\n"+
			"It is a starter output for the Planwright AWS web application pattern. Review the files before running Terraform or OpenTofu commands. Planwright does not run provider installation, validation, planning or apply steps. Review constraints:\n"+
			"- Provider: %s\n"+
			"- Region: %s\n"+
			"- Profile: %s\n"+
			"- Lab networking: ECS tasks are placed in generated public subnets with `assign_public_ip = true` so the starter pack avoids a NAT Gateway. Treat this as a review/lab trade-off; move tasks to private egress before production-like use.\n",
		markdownCode(g.Provider),
		markdownCode(g.Region),
		markdownCode(effectiveProfile(g)),
	)
}

